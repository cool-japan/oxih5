#[cfg(test)]
mod tests {
    use super::*;
    use oxih5::{Dtype, FileWriter};

    /// Write a minimal HDF5 file containing one f64 dataset, return bytes.
    fn make_simple_h5(name: &str, data: &[f64]) -> Vec<u8> {
        let tmp = std::env::temp_dir().join(format!("oxinc_test_{name}.h5"));
        FileWriter::new()
            .write_dataset_f64(name, data, &[data.len()])
            .unwrap()
            .build(&tmp)
            .unwrap();
        std::fs::read(&tmp).unwrap()
    }

    #[test]
    fn test_open_from_bytes_simple() {
        let bytes = make_simple_h5("temperature", &[1.0, 2.0, 3.0]);
        let nc = NcFile::open_from_bytes(&bytes).unwrap();
        let root = nc.root_group().unwrap();
        // Should find "temperature" dataset as a variable
        let var = root
            .variable("temperature")
            .expect("should find temperature");
        assert_eq!(var.shape, vec![3]);
        assert!(matches!(var.dtype, Dtype::Float { size: 8, .. }));
    }

    #[test]
    fn test_phony_dims_for_undimensioned_variable() {
        // A plain HDF5 dataset with no DIMENSION_LIST gets phony dims.
        let bytes = make_simple_h5("lat", &[10.0, 20.0, 30.0, 40.0]);
        let nc = NcFile::open_from_bytes(&bytes).unwrap();
        let root = nc.root_group().unwrap();
        let var = root.variable("lat").unwrap();
        assert_eq!(var.dims.len(), 1);
        // Phony dim names start with "phony_dim_"
        assert!(
            var.dims[0].name.starts_with("phony_dim_"),
            "expected phony dim name, got: {}",
            var.dims[0].name
        );
        assert_eq!(var.dims[0].len, 4);
    }

    #[test]
    fn test_root_group_name() {
        let bytes = make_simple_h5("x", &[1.0]);
        let nc = NcFile::open_from_bytes(&bytes).unwrap();
        let root = nc.root_group().unwrap();
        assert_eq!(root.name, "/");
        assert_eq!(root.path, "/");
    }

    #[test]
    fn test_no_dimensions_scalar() {
        // Scalar dataset (shape=[]) should produce no axes.
        // FileWriter doesn't expose scalar write yet; use a length-1 array as proxy.
        let bytes = make_simple_h5("scalar", &[42.0]);
        let nc = NcFile::open_from_bytes(&bytes).unwrap();
        let root = nc.root_group().unwrap();
        assert!(root.variable("scalar").is_some());
    }
}
